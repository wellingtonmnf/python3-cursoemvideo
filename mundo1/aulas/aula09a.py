frase = 'Curso em Vídeo Python  '

# Fatiamento de strings

print(frase) # frase inteira
print(frase[3]) # 4ª posição do vetor de string
print(frase[3:13]) # 4ª até a 12ª posição do vetor de string
print(frase[:13]) # início até a 12ª posição do vetor de string
print(frase[13:]) # 12ª até o final do vetor de string
print(frase[1:15]) # 1ª até a 14ª posição do vetor de string
print(frase[1:15:2]) # 1ª até a 14ª posição do vetor de string, pulando de 2 em 2 posições
print(frase[1::2]) # 1ª até a última posição do vetor de string, pulando de 2 em 2 posições
print(frase[::2]) # do início até a última posição do vetor de string, pulando de 2 em 2 posições

